# პარამეტრად უნდა მიიღოს შემდეგი სია [100,20,30,50,5323,3321,22,56,700,90,10]


def total_sum(list):
    sum = 0
    for elem in list:
        sum += elem
        
    return sum

lst = [100,20,30,50,5323,3321,22,56,700,90,10]

print(total_sum(lst))