# დაწერეთ პითნის ფუნქცია, რომელიც იღებს პარამეტრად ერთი და იგივე ზომის სიას(list) და 
# zip ფუნქციის გამოყენებით დააჯგუფეთ სიების ელემენტები ერთმანეთთან. 
# params: [1, 2, 3], ['a', 'b', 'c'] 
# outputs: ["(1, 'a')", "(2, 'b')", "(3, 'c')"]


list1 = [1, 2, 3]
list2 = ['a', 'b', 'c'] 

def zipper(a, b):
    return list(zip(a, b))

print(zipper(list1, list2))